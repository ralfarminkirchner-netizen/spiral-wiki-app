import { Routes, Route, Link, useParams, useNavigate } from 'react-router-dom';
import { useState, useEffect } from 'react';
import ReactMarkdown from 'react-markdown';
import remarkGfm from 'remark-gfm';

// Custom Hook für lokalen Speicher
function useLocalStorage<T>(key: string, initialValue: T): [T, (value: T | ((val: T) => T)) => void] {
  const [storedValue, setStoredValue] = useState<T>(() => {
    try {
      const item = window.localStorage.getItem(key);
      return item ? JSON.parse(item) : initialValue;
    } catch (error) {
      console.warn(error);
      return initialValue;
    }
  });

  const setValue = (value: T | ((val: T) => T)) => {
    try {
      const valueToStore = value instanceof Function ? value(storedValue) : value;
      setStoredValue(valueToStore);
      window.localStorage.setItem(key, JSON.stringify(valueToStore));
    } catch (error) {
      console.warn(error);
    }
  };
  return [storedValue, setValue];
}


// The data is generated by the build script and placed in public/data.json
interface Monograph {
  id: string;
  title: string;
  category: string;
  content: string;
  imageUrl?: string | null;
}

interface CategoryNode {
  name: string;
  items: Monograph[];
  subcats: Record<string, CategoryNode>;
}

function buildTree(data: Monograph[]): CategoryNode {
  const root: CategoryNode = { name: 'Root', items: [], subcats: {} };
  for (const item of data) {
    let current = root;
    // Categories are stored like "Philosophie / Existenzialismus"
    const pathParts = item.category ? item.category.split(' / ') : ['Uncategorized'];
    
    for (const part of pathParts) {
      if (!current.subcats[part]) {
        current.subcats[part] = { name: part, items: [], subcats: {} };
      }
      current = current.subcats[part];
    }
    current.items.push(item);
  }
  return root;
}

function TreeNode({ node, level, search, readMonographs }: { node: CategoryNode, level: number, search: string, readMonographs: string[] }) {
  const hasSearch = search.trim().length > 0;
  // Automatically expand if searching, otherwise collapsed by default
  const [isOpen, setIsOpen] = useState(hasSearch || level === 0);

  // Sync open state with search
  useEffect(() => {
    if (hasSearch) setIsOpen(true);
  }, [hasSearch]);

  const subcatNames = Object.keys(node.subcats).sort();
  const hasSubcats = subcatNames.length > 0;
  const hasItems = node.items.length > 0;

  if (!hasSubcats && !hasItems) return null;

  // Calculate read progress for this node (recursive)
  const getAllItems = (n: CategoryNode): Monograph[] => {
    let all = [...n.items];
    for (const sub of Object.values(n.subcats)) {
      all = [...all, ...getAllItems(sub)];
    }
    return all;
  };
  
  const allNodeItems = getAllItems(node);
  const readCount = allNodeItems.filter(item => readMonographs.includes(item.id)).length;
  const totalCount = allNodeItems.length;
  const progress = totalCount > 0 ? Math.round((readCount / totalCount) * 100) : 0;

  return (
    <div className={`tree-node level-${level}`}>
      <div 
        className={`tree-header glass ${isOpen ? 'open' : ''} ${level === 0 ? 'root-header' : ''}`}
        onClick={() => setIsOpen(!isOpen)}
      >
        <div className="tree-title-row">
          <span className="expand-icon">{isOpen ? '▼' : '▶'}</span>
          <span className="tree-name">{node.name.replace(/_/g, ' ')}</span>
        </div>
        <div className="tree-stats">
          <span className="tree-count">{readCount}/{totalCount} ({progress}%)</span>
        </div>
      </div>
      
      {isOpen && (
        <div className="tree-children animate-slide-up">
          {subcatNames.map(name => (
            <TreeNode 
              key={name} 
              node={node.subcats[name]} 
              level={level + 1} 
              search={search}
              readMonographs={readMonographs}
            />
          ))}
          
          {hasItems && (
            <ul className="monograph-list">
              {node.items.sort((a, b) => a.title.localeCompare(b.title)).map(item => {
                const isRead = readMonographs.includes(item.id);
                return (
                  <li key={item.id}>
                    <Link to={`/monograph/${item.id}`} className={`monograph-link ${isRead ? 'read' : ''}`}>
                      {item.imageUrl && (
                        <div className="monograph-avatar-wrapper">
                          <img src={item.imageUrl} alt={item.title} className="monograph-avatar-img" loading="lazy" />
                          {isRead && <div className="read-badge">✓</div>}
                        </div>
                      )}
                      {!item.imageUrl && isRead && <span className="read-badge-text">✓</span>}
                      <span>{item.title}</span>
                    </Link>
                  </li>
                );
              })}
            </ul>
          )}
        </div>
      )}
    </div>
  );
}

function Dashboard({ data, readMonographs }: DashboardProps) {
  const [search, setSearch] = useState('');
  
  const filteredData = data.filter(d => 
    d.title.toLowerCase().includes(search.toLowerCase()) || 
    d.category.toLowerCase().includes(search.toLowerCase())
  );

  const tree = buildTree(filteredData);
  const mainCategoriesCount = Object.keys(tree.subcats).length;

  return (
    <div className="dashboard-container animate-fade-in">
      <header className="hero">
        <h1 className="gradient-text">SPiRAL MiND WiKi</h1>
        <div className="search-bar">
          <input 
            type="text" 
            placeholder="Suche nach Denkern oder Disziplinen..." 
            value={search}
            onChange={e => setSearch(e.target.value)}
          />
        </div>
      </header>

      <div className="stats-container">
        <div className="stat-box glass">
          <h3>Einträge</h3>
          <p className="stat-number">{filteredData.length}</p>
        </div>
        <div className="stat-box glass">
          <h3>Hauptordner</h3>
          <p className="stat-number">{mainCategoriesCount}</p>
        </div>
        <div className="stat-box glass highlight-box">
          <h3>Erforscht</h3>
          <p className="stat-number">{readMonographs.length}</p>
          <div className="progress-bar-container">
            <div className="progress-bar-fill" style={{ width: `${Math.round((readMonographs.length / data.length) * 100)}%` }}></div>
          </div>
          <p className="progress-text">{Math.round((readMonographs.length / data.length) * 100)}% gesamt</p>
        </div>
      </div>

      <div className="tree-container">
        {Object.keys(tree.subcats).sort().map(catName => (
          <TreeNode 
            key={catName} 
            node={tree.subcats[catName]} 
            level={0} 
            search={search}
            readMonographs={readMonographs}
          />
        ))}
      </div>
    </div>
  );
}

interface ReaderProps {
  data: Monograph[];
  markAsRead: (id: string) => void;
}

function MonographReader({ data, markAsRead }: ReaderProps) {
  const { id } = useParams<{ id: string }>();
  const navigate = useNavigate();
  const monograph = data.find(d => d.id === id);

  useEffect(() => {
    if (id && monograph) {
      markAsRead(id);
    }
  }, [id, monograph, markAsRead]);

  if (!monograph) {
    return (
      <div className="reader-container not-found glass">
        <h2>Monographie nicht gefunden</h2>
        <button className="btn-primary" onClick={() => navigate('/')}>Zurück zur Bibliothek</button>
      </div>
    );
  }


  return (
    <div className="reader-wrapper animate-slide-up">
      <nav className="reader-nav glass">
        <button className="btn-back" onClick={() => navigate('/')}>
          <span className="icon">←</span> Zurück
        </button>
        <span className="category-badge">{monograph.category}</span>
      </nav>
      
      <article className="reader-content glass">
        <ReactMarkdown 
          remarkPlugins={[remarkGfm]}
          components={{
            a: ({node, ...props}) => {
              const href = props.href || '';
              // Interne Links via React Router leiten
              if (href.startsWith('/monograph/')) {
                return <Link to={href} className="wiki-link">{props.children}</Link>;
              }
              // Externe Links klassisch öffnen
              return <a target="_blank" rel="noopener noreferrer" className="external-link" {...props}>{props.children}</a>;
            }
          }}
        >
          {monograph.content}
        </ReactMarkdown>
      </article>
    </div>
  );
}

export default function App() {
  const [data, setData] = useState<Monograph[]>([]);
  const [loading, setLoading] = useState(true);
  const [readMonographs, setReadMonographs] = useLocalStorage<string[]>('spiral-wiki-read', []);

  const markAsRead = (id: string) => {
    setReadMonographs(prev => {
      if (!prev.includes(id)) {
        return [...prev, id];
      }
      return prev;
    });
  };

  useEffect(() => {
    fetch('/data.json')
      .then(res => res.json())
      .then(json => {
        setData(json);
        setLoading(false);
      })
      .catch(err => {
        console.error("Failed to load wiki data", err);
        setLoading(false);
      });
  }, []);

  if (loading) {
    return <div className="loader-container"><div className="spinner"></div><p>Lade SPiRAL MiND WiKi...</p></div>;
  }

  return (
    <div className="app-layout">
      <Routes>
        <Route path="/" element={<Dashboard data={data} readMonographs={readMonographs} />} />
        <Route path="/monograph/:id" element={<MonographReader data={data} markAsRead={markAsRead} />} />
      </Routes>
    </div>
  );
}
